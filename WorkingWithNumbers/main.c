#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    printf("%f\n", 5 + 4.5);
    printf("%f\n", 5 / 4.0);

    int num = 6;
    printf("%d\n", num);

    printf("%f\n", pow(2, 3));
    printf("%f\n", sqrt(36));
    printf("%f\n", ceil(36.656));
    printf("%f\n", floor(36.656));

    return 0;
}
